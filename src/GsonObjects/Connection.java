package GsonObjects;

public class Connection {
    private String username;
    private long since;
    private int visits;
    private int likes;
    private int comments;

    public String getUsername() {
        return username;
    }

    public long getSince() {
        return since;
    }

    public int getVisits() {
        return visits;
    }

    public int getLikes() {
        return likes;
    }

    public int getComments() {
        return comments;
    }
}
